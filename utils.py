"""
Module that is a simple calculator
"""


def add(a: int, b: int) -> int:
    """Adds two ints together"""
    return a + b


def substract(a: int, b: int) -> int:
    """Substract the second int from the first one"""
    return a - b


def multiply(a: int, b: int) -> int:
    """Multiply two ints"""
    return a * b


def divide(a: int, b: int) -> float:
    """Divides first int by the second one"""
    return a / b
